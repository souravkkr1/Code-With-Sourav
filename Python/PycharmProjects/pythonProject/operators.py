num1 = 10
num2 = 2

# Arithmetic Operators
print("num1 + num2 is ", num1 + num2)
print("num1 - num2 is ", num1 - num2)
print("num1 * num2 is ", num1 * num2)
print("num1 / num2 is ", num1 / num2)
print("num1 // num2 is ", num1 // num2)
print("num1 ** num2 is ", num1 ** num2)
print("num1 % num2 is ", num1 % num2)

# Assignment Operators
a = 4
a += 2
print(a)

# Comparison Operators
x = 8
y = 3
z = 8
print(x>y)
print(x<y)
print(x!=y)
print(x==z)

# Logical Operator
print(x==z and x<y)
print(x==z or x<y)
print(not(False))
print(not(True))
